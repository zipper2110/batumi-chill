package com.litvin.batumichill.model

/**
 * Represents the coolness rating of a location.
 * Used to indicate how worthwhile a location is to visit.
 */
enum class CoolnessRating {
    MUST_SEE,    // Absolutely must visit this location
    COOL,        // Worth visiting if you have time
    JUST_OK      // Only if you have extra time
}